ASSUME DS:DATA CS:CODE

DATA SEGMENT
    LIST DB 04H,02H,03H,04H,05H
    ODD DB 01 DUP(0)
    EVEN DB 01 DUP(0)
DATA ENDS

CODE SEGMENT
START:
    MOV AX, DATA
    MOV DS, AX


    MOV CL, 05H
    MOV SI, OFFSET LIST

PROCESS_LOOP:
    MOV AL, [SI]
    RCR AL, 01
    JNC EVEN_SEGMENT
    INC ODD
    JMP NEXT_ELEMENT

EVEN_SEGMENT:
    INC EVEN

NEXT_ELEMENT:
    INC SI
    DEC CL
    JNZ PROCESS_LOOP



    MOV DL, EVEN
    MOV DH, ODD
    MOV AH, 4CH
    INT 21H

CODE ENDS
END START
